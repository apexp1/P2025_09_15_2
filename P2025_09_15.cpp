char Sprites[10] = { ' ', '+', '@' , '@' }; //그림으로 바꾸자

char KeyCode;

void Input() {
    KeyCode = _getch();
}

void process(){

    switch (KeyCode)
    {
    case 'w':
        if (IsRun(PlayerY, PlayerX-1)){
            PlayerX--;
        }
        break;

    case 'a':
        if (IsRun(PlayerY-1, PlayerX)) {        
            PlayerY--;
        }
       
        break;

    case 's':
        if (IsRun(PlayerY, PlayerX + 1)) {
            PlayerX++;
        }
        
        break;

    case 'd':
        if (IsRun(PlayerY+1, PlayerX)) {
            PlayerY++;
        }
        break;

    case 'q':
        bIsRunning = false;
        break;

    default:
        break;
    }

   
    
}


