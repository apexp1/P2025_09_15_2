bool IsRun(int _X, int _Y) {

    if (World[_Y][_X] == 0) {
        return true;
    }else if (World[_Y][_X] == 3) {
        cout << "충돌행ㅆ다." << endl;
        return false;
    }
    else {
        return false;
    }
    
}



